#!/bin/bash

#######################################################
#作用:
#  本脚本的作用在于统计指定文件夹内，指定类型代码的行数
#选项:
# 
#
#作者:aideny
#######################################################


# 获取要统计的文件目录
path=$1

if test -z ${path}
then
  echo "Usage: ./cl.sh [filepath]"
else # 进行统计
  wc -l `find ${path} -name "*.cc"` | tail -n1
fi



